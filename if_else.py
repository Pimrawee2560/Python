score = 58

if score >= 80:  # score ได้มากกว่า 80
    print(' Grade A') #ถ้าได้ 80 จะได้ Grade A
elif score >= 70: # score ได้มากกว่า 70
    print('Grade B') #ถ้าได้มากกว่า 70 จะได้ Grade B
elif score >= 60:# score ได้มากกว่า 60
    print('Grade C')#ถ้าได้มากกว่า 60 ได้ Grade C
else:
    print('Grade F')