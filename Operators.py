x = 5
y = 2

a1 = x + y    # + = บวก 
print(a1) # 7

a2 = x - y    # - = ลบ
print(a2)   # 3

a3 = x * y  # * =คูณ
print(a3)  # 10

a4 = x / y   # / = หาร
print(a4)  # 2.5

a5 = x % y  # % = หาเศษจากการหาร
print(a5)  # 1

a6 = x ** y  #  ** = ยกกำลัง
print(a6) # 25

a7 = x // y   # // = หารปัดเศษทิ้ง
print(a7) # 2

a8 = (x+1) * ( y-1)  # ( ) เพื่อแยกสมการ
print(a8) # 6